#include "libft.h"

char	*ft_strdup(const char *s)
{
	char			*str;
	unsigned int	i;

	str = malloc(sizeof(char) * ft_strlen(s) + 1);
	if (!str)
		return (NULL);
	i = 0;
	while (s[i])
	{
		str[i] = (char) s[i];
		i++;
	}
	str[i] = 0;
	return (str);
}
